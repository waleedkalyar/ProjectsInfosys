public class InstituteModel {
    public int instThumnail;
    public String instName;
    public String instLocation;
    public String instPositions;
    public String instStudents;

    public InstituteModel(int instThumnail, String instName, String instLocation, String instStudents, String instPositions) {
        this.instThumnail = instThumnail;
        this.instName = instName;
        this.instLocation = instLocation;
        this.instPositions = instPositions;
        this.instStudents = instStudents;

    }
}
